package com.example.hydrologicalWeather.enity;

public class Peak {

    private Integer id;
    private String x;
    private String xEnd;
    private Integer y;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getxEnd() {
        return xEnd;
    }

    public void setxEnd(String xEnd) {
        this.xEnd = xEnd;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
